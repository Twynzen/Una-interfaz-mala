    private void BotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGuardarActionPerformed
        String a = Espaciocliente.getText();
        String b = Espaciotransaccion.getText();
        String c = Espaciofechayhora.getText();
        int  d = Integer.parseInt(b);
        Botones.guardar(a, d, c);
        
        Espaciocliente.setText("");
        Espaciotransaccion.setText("");
        Espaciofechayhora.setText("");
        
    }//GEN-LAST:event_BotonGuardarActionPerformed

    private void BotonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonBuscarActionPerformed
        String buscar = buscartxt.getText();
        Botones.buscar(buscar);
        
        buscartxt.setText("");
    }//GEN-LAST:event_BotonBuscarActionPerformed
    public void cargardatos(Persona per){
        this.Espaciocliente.setText(per.getNombre());
        this.Espaciotransaccion.setText(Integer.toString(per.getTransaccion()));
        this.Espaciofechayhora.setText(per.getFecha());
        
    }
